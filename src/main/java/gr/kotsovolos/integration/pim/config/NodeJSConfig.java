package gr.kotsovolos.integration.pim.config;

public class NodeJSConfig {

	private String nodeCommand;
	private String nodeJSApllicationLocation;
	private String nodeJSApplicationName;

	public String getNodeJSApllicationLocation() {
		return nodeJSApllicationLocation;
	}

	public void setNodeJSApllicationLocation(String nodeJSApllicationLocation) {
		this.nodeJSApllicationLocation = nodeJSApllicationLocation;
	}

	public String getNodeJSApplicationName() {
		return nodeJSApplicationName;
	}

	public void setNodeJSApplicationName(String nodeJSApplicationName) {
		this.nodeJSApplicationName = nodeJSApplicationName;
	}

	public String getNodeCommand() {
		return nodeCommand;
	}

	public void setNodeCommand(String nodeCommand) {
		this.nodeCommand = nodeCommand;
	}

}
